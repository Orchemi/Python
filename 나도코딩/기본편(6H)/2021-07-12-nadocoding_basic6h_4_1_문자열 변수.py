# 문자열 변수
sentence = '나는 소년입니다'
print(sentence)

sentence2 = "파이썬은 쉬워요"
print(sentence2)


# 여러 문장의 문자열을 변수로 지정할 때에는 큰따옴표 3개 안에 넣는다.
sentence3 = """
나는 소년이고,
파이썬은 쉬워요"""
print(sentence3)



