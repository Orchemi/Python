# 숫자 처리 함수

print(abs(-5))  # -5의 절댓값 : 5
print(pow(4, 2))  # 4를 2번 곱함 or 4의 2제곱 : 16
print(max(5, 12))  # maximum
print(min(5, 12))  # minimum
print(round(3.14))  # 3.14의 반올림 값 : 3
print(round(4.99))  # 5의 반올림 값 : 5


# math library 이용하기
from math import *  # "math library 안의 모든 것을 이용하겠다."

print(floor(4.99))  # 내림값 : 4
print(ceil(3.14))  # 올림값 : 4
print(sqrt(16))  # 제곱근 : 4





