# 간단한 수식
print(2 + 3 * 4)  # 2 + 12 = 14
print((2 + 3) * 4)  # 5 * 4 = 20


# 변수 이용
number = 2 + 3 * 4  # 14
print(number)  # 14
number = number + 2  # 14 + 2
print(number)  # 16 ; 앞에서 재설정 되었기 때문


# 수식의 고급화
#   위와 아래의 식은 같은 의미이다.
number = number + 2
number += 2

number = number * 2
number *= 2

number = number / 2
number /= 2

number = number - 2
number -= 2





